#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	result;
	int	flag;

	i = 0;
	flag = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		flag = 1;
		i++;
	}
	if (str[i] == '+' && flag != 1)
		i++;
	result = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	if (flag == 1)
		result *= -1;
	return (result);
}
